public class DaireHesap{

    public static void main(String[] args) {
       double yaricap = 5.5 ;
       double pi = 3.14159;
       double dairecevre = pi * 2 * yaricap;
       double dairealan = pi * yaricap * yaricap;
       System.out.println("Yarıçap : "+yaricap);
       System.out.println("Yarıçapı 5.5 Olan Dairenin Çevresi : " + dairecevre);
       System.out.println("Yarıçapı 5.5 Olan Dairenin Alanı : " + dairealan);

    }

}
